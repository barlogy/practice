#!/usr/bin/python
import numpy as np

"""
Игра "Угадай число"
Компьютер загадывает число от 1 до 100, а пользователь пытается его угадать.
"""


def guess_number() -> None:
    """
    Основная логика игры.

    Компьютер генерирует случайное число, пользователь вводит догадки
    и получает подсказки ("больше" или "меньше") до тех пор,
    пока не угадает загаданное число.
    """
    print("Добро пожаловать в игру 'Угадай число'!")
    print("Компьютер загадал число от 1 до 100. Попробуйте угадать его!")

    # Генерируем случайное число от 1 до 100
    number = np.random.randint(1, 101)
    count = 0  # Счетчик попыток

    while True:
        try:
            # Получаем число от пользователя
            predict_number = int(input("Введите число от 1 до 100: "))

            # Проверяем корректность введенного значения
            if predict_number < 1 or predict_number > 100:
                print("Ошибка! Число должно быть в диапазоне от 1 до 100.")
                continue

            # Увеличиваем счетчик попыток
            count += 1

            # Проверяем догадку пользователя
            if predict_number > number:
                print("Загаданное число меньше!")
            elif predict_number < number:
                print("Загаданное число больше!")
            else:
                print(f"Поздравляем! Вы угадали число {number} за {count} попыток!")
                break  # Выход из цикла при угадывании

        except ValueError:
            # Обрабатываем ошибку при вводе нечислового значения
            print("Ошибка! Пожалуйста, введите целое число.")


if __name__ == "__main__":
    guess_number()
